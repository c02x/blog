<template>
  <section class="app-main">
    <transition name="fade-transform" mode="out-in">
      <keep-alive :include="cachedViews">
        <router-view :key="key" />
      </keep-alive>
    </transition>
  </section>
</template>

<script>
export default {//用于渲染应用的主要内容区域。
  // 该组件利用了Vue Router的<router-view>和Vue的<transition>及<keep-alive>特性，实现了页面内容的动态切换和缓存。
  name: 'AppMain',
  computed: {
    cachedViews() {
      return this.$store.state.tagsView.cachedViews
    },
    key() {
      return this.$route.path
    }
  }
}
</script>

